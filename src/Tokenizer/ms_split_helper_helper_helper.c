#include "minishell.h"

int	ft_isspace(char c)
{
	return (c == ' ' || c == '\t' || c == '\n' || c == '\v' || c == '\f'
		|| c == '\r');
}

bool	ft_is_operator(char c)
{
	return (c == '|' || c == '<' || c == '>');
}
